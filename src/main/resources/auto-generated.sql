       add constraint FK_player_transfer_history_player
       foreign key (player_id)
       references player;

    alter table if exists player_transfer_history
       add constraint FK_player_transfer_history_from_team
       foreign key (from_team_id)
       references team;

    alter table if exists player_transfer_history
       add constraint FK_player_transfer_history_to_team
       foreign key (to_team_id)
       references team;

