
    create sequence team_seq start with 1 increment by 1;

    create table manager (
        id integer generated by default as identity,
        is_active boolean default true,
        team_id integer unique,
        created_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        updated_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        name varchar(255) not null,
        primary key (id)
    );

    comment on column manager.name is
        '監督名';

    create table player (
        id integer generated by default as identity,
        is_active boolean default true not null,
        team_id integer not null,
        uniform_number integer not null,
        created_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        updated_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        position varchar(16) not null,
        name varchar(64) not null,
        primary key (id),
        unique (team_id, uniform_number)
    );

    comment on column player.uniform_number is
        '背番号';

    comment on column player.position is
        'ポジション';

    comment on column player.name is
        '選手名';

    create table player_transfer_history (
        id integer generated by default as identity,
        player_id integer not null,
        from_team_id integer,
        to_team_id integer not null,
        transferred_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        created_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        primary key (id),
        check (from_team_id is null or from_team_id <> to_team_id)
    );

    comment on column player_transfer_history.transferred_at is
        '移籍日時';

    create index idx_player_transfer_history_player_transferred_at
        on player_transfer_history (player_id, transferred_at);

    create table team (
        id integer not null,
        is_active boolean default true not null,
        created_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        updated_at timestamp DEFAULT CURRENT_TIMESTAMP not null,
        name varchar(64) not null unique,
        primary key (id)
    );

